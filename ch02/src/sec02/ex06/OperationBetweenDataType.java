package sec02.ex06;

public class OperationBetweenDataType {

	public static void main(String[] args) {
		int value1 = 3 + 5;
		int value2 = 8 / 5;
		float value3 = 3.0f + 5.0f;
		double value4 = 8.0 / 5.0;
		
		System.out.printf("%d, %d, %f, %f", value1, value2, value3, value4);
	}

}
